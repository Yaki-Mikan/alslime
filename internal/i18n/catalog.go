// ErrInvalidLang は言語コードがファイル名断片として不正な場合に返る。
var ErrInvalidLang = errors.New(KeyErrorInvalidLang)

var langPattern = regexp.MustCompile(`^[A-Za-z][A-Za-z0-9-]{0,31}$`)

// Catalog は UI 辞書レスポンス。
type Catalog struct {
	Lang         string            `json:"lang"`
	DefaultLang  string            `json:"defaultLang"`
	FallbackLang string            `json:"fallbackLang"`
	Messages     map[string]string `json:"messages"`
}

// Service は WORKSPACE_ROOT 配下の i18n 辞書と内蔵辞書を合成する。
//
// Load はチャット送信のたび（resolvePromptLocale）と UI 起動・言語切替時
// （GET /api/i18n/{lang}）に呼ばれるため、マージ済みカタログを lang 別に
// キャッシュする。内蔵辞書はコンパイル時定数で不変、外部辞書は stamp
// （存在有無＋mtime＋size）の一致で変更なしとみなす。キャッシュした
// Messages マップは読み取り専用として共有する（呼び出し側は参照のみ）。
type Service struct {
	resolver *paths.Resolver
	baseDir  string
	mu       sync.Mutex
	cache    map[string]catalogCacheEntry
}

type catalogCacheEntry struct {
	catalog       Catalog
	fallbackStamp externalStamp
	langStamp     externalStamp
}

// externalStamp は外部辞書ファイルの変更検出用スタンプ。未存在はゼロ値。
type externalStamp struct {
	exists  bool
	modTime time.Time
	size    int64
}

func (a externalStamp) equal(b externalStamp) bool {
	return a.exists == b.exists && a.size == b.size && a.modTime.Equal(b.modTime)
}

// New は Service を生成する。
func New(resolver *paths.Resolver, baseDir string) *Service {
	return &Service{resolver: resolver, baseDir: baseDir, cache: map[string]catalogCacheEntry{}}
}

// Languages は内蔵辞書と外部辞書から利用可能な言語コードを返す。
func (s *Service) Languages() ([]string, error) {
	seen := map[string]bool{}
	for lang := range builtinCatalogs {
		seen[lang] = true
	}
	dir, err := s.resolver.ResolveLexical(s.baseDir)
	if err != nil {
		return nil, err
	}
	if _, err := os.Lstat(dir); err == nil {
		realDir, err := s.resolver.ResolveExisting(s.baseDir)
		if err != nil {
			return nil, err
		}
		entries, err := os.ReadDir(realDir)
		if err != nil {
			return nil, err
		}
		for _, entry := range entries {
			if entry.IsDir() {
				continue
			}
			name := entry.Name()
			if len(name) <= len(".json") || name[len(name)-len(".json"):] != ".json" {
				continue
			}
			lang := name[:len(name)-len(".json")]
			if validLang(lang) {
				seen[lang] = true
			}
		}
	} else if !errors.Is(err, fs.ErrNotExist) {
		return nil, err
	}
	out := make([]string, 0, len(seen))
	for lang := range seen {
		out = append(out, lang)
	}
	sort.Strings(out)
	return out, nil
}

// Load は指定言語の辞書を返す。未翻訳キーは fallback → default の順で補う。
//
// マージ順（後勝ち）: 内蔵default → 内蔵fallback → 外部fallback → 内蔵lang → 外部lang。
// fallback 系（未翻訳キーの補完）は必ず lang 系より前に置くこと。
// 旧実装は外部fallbackを内蔵langの後にマージしていたため、例えば lang=ja のとき
// WORKSPACE の i18n/en.json にあるキーが内蔵 ja の日本語を英語で上書きしていた（02調査 中#4）。
func (s *Service) Load(lang string) (Catalog, error) {
	if lang == "" {
		lang = config.I18NDefaultLang
	}
	if !validLang(lang) {
		return Catalog{}, ErrInvalidLang
	}
	// 外部辞書（fallback / lang）の stamp が前回構築時と一致すれば
	// マージ済みカタログを再利用し、毎回の 5 マージ＋外部パースを省く。
	fallbackStamp := s.externalStamp(config.I18NFallbackLang)
	langStamp := s.externalStamp(lang)
	s.mu.Lock()
	if entry, ok := s.cache[lang]; ok && entry.fallbackStamp.equal(fallbackStamp) && entry.langStamp.equal(langStamp) {
		s.mu.Unlock()
		return entry.catalog, nil
	}
	s.mu.Unlock()

	messages := map[string]string{}
	merge(messages, builtinCatalogs[config.I18NDefaultLang])
	merge(messages, builtinCatalogs[config.I18NFallbackLang])
	if lang != config.I18NFallbackLang {
		if external, err := s.loadExternal(config.I18NFallbackLang); err == nil {
			merge(messages, external)
		} else if !errors.Is(err, fs.ErrNotExist) {
			return Catalog{}, err
		}
	}
	merge(messages, builtinCatalogs[lang])
	if external, err := s.loadExternal(lang); err == nil {
		merge(messages, external)
	} else if !errors.Is(err, fs.ErrNotExist) {
		return Catalog{}, err
	}
	catalog := Catalog{
		Lang:         lang,
		DefaultLang:  config.I18NDefaultLang,
		FallbackLang: config.I18NFallbackLang,
		Messages:     messages,
	}
	s.mu.Lock()
	s.cache[lang] = catalogCacheEntry{catalog: catalog, fallbackStamp: fallbackStamp, langStamp: langStamp}
	s.mu.Unlock()
	return catalog, nil
}

// externalStamp は外部辞書ファイルの現在のスタンプを返す。
// パス解決不能・未存在・Stat 失敗はいずれも「存在しない」として扱う
//（その状態自体がキャッシュ検証の比較対象になる）。
func (s *Service) externalStamp(lang string) externalStamp {
	lexical, err := s.resolver.ResolveLexical(s.baseDir + "/" + lang + ".json")
	if err != nil {
		return externalStamp{}
	}
	info, err := os.Stat(lexical)
	if err != nil {
		return externalStamp{}
	}
	return externalStamp{exists: true, modTime: info.ModTime(), size: info.Size()}
}

func (s *Service) loadExternal(lang string) (map[string]string, error) {
	rel := s.baseDir + "/" + lang + ".json"
	lexical, err := s.resolver.ResolveLexical(rel)
	if err != nil {
		return nil, err
	}
	if _, err := os.Lstat(lexical); err != nil {
		return nil, err
	}
	path, err := s.resolver.ResolveExisting(rel)
	if err != nil {
		return nil, err
	}
	var data map[string]string
	if err := jsonstore.ReadJSON(path, &data); err != nil {
		return nil, err
	}
	return data, nil
}

func validLang(lang string) bool {
	return langPattern.MatchString(lang)
}

func merge(dst map[string]string, src map[string]string) {
	for k, v := range src {
		dst[k] = v
	}
}

