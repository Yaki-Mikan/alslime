const localizeTree = <T extends TextTree>(uiCatalog: I18NCatalog | null, prefix: string, source: T): T => {
    if (typeof source === 'string') {
        const fallback = uiCatalog?.lang?.startsWith('en') ? COMFYUI_TEXT_FALLBACK_EN[prefix] || source : source;
        return resolveMessage(uiCatalog, prefix, fallback) as T;
    }
    if (typeof source === 'number') {
        return source;
    }

    const localized: Record<string, TextTree> = {};
    for (const [key, value] of Object.entries(source)) {
        localized[key] = localizeTree(uiCatalog, `${prefix}.${key}`, value);
    }
    return localized as T;
};

